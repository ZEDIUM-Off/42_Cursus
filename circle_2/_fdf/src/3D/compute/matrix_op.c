#include "geom.h"

t_mat4	translate(t_vec3	*pos)
{
	t_mat4	res;

	res = def_mat();
	res[0][0] = 1;
	res[1][1] = 1;
	res[2][2] = 1;
	res[3][0] = pos->x;
	res[3][1] = pos->y;
	res[3][2] = pos->z;
	res[3][3] = 1;
	return (res);
}
